#include "so_long.h"

void	copy_map(char *s, char *str)
{
	size_t	i;
	size_t	j;

	j = 0;
	i = 0;
	if (!str)
		return ;
	while (s[i] && s[i] != '\n' && s[i] != '\0')
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
}
